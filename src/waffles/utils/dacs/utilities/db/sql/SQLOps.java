package waffles.utils.dacs.utilities.db.sql;

/**
 * The {@code SQLOps} enum defines {@code Database} operations.
 *
 * @author Waffles
 * @since 02 Nov 2025
 * @version 1.1
 */
public enum SQLOps
{
	/**
	 * A delete operation.
	 */
	DELETE,
	/**
	 * A exists operation.
	 */
	EXISTS,
	/**
	 * A insert operation.
	 */
	INSERT,
	/**
	 * A select operation.
	 */
	SELECT,
	/**
	 * A update operation.
	 */
	UPDATE;
}
